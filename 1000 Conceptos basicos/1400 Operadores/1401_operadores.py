# Operadores matemáticos

a = 2
b = 3
print()

    # Suma
suma = a + b
print(f"El resultado de sumar {a} + {b} es {suma}")
print()

    # Resta
resta = a - b
print(f"El resultado de restar {a} - {b} es {resta}")
print()

    # Multiplicación
multiplicacion = a * b
print(f"El resultado de multiplicar {a} * {b} es {multiplicacion}")
print()

    # División
division = a / b
print(f"El resultado de dividir {a} / {b} es {division}")
print()

    # Modulo o resto de la división
resto = a % b
print(f"El resto de la división entre  {a} y {b} es {resto}")
print()

    # Potencia
potencia = a ** b
print(f"La potencia de {a} a la {b} es {potencia}")
print()

    # Concatenar texto usando el operador de suma
texto1 = "Curso introductorio "
texto2 = "a Python"
texto_concatenado = texto1 + texto2
print(texto_concatenado)
print()

    # Uso del operador de multiplicación para duplicar texto
print("Texto1" * 3)
print()
