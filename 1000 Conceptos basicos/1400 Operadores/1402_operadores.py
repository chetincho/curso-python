# Operadores lógicos o comparativos

a = 2
b = 3
print()

    # Mayor que
respuesta = a > b
print(f"¿ {a} es mayor que {b} ? {respuesta}")
print()

    # Menor que
respuesta = a < b
print(f"¿ {a} es menor que {b} ? {respuesta}")
print()

    # Mayor o igual que
respuesta = a >= b
print(f"¿ {a} es mayor o igual que {b} ? {respuesta}")
print()

    # Menor o igual que
respuesta = a <= b
print(f"¿ {a} es menor o igual que {b} ? {respuesta}")
print()

    # Igual
respuesta = a == b
print(f"¿ {a} es igual que {b} ? {respuesta}")
print()

    # Nótese que el operador de igualdad también puede utilizarse con cadenas de texto
respuesta = "Texto1" == "Texto1" 
print(f"¿ Texto1 es igual que Texto1 ? {respuesta}")
print()

    # Distinto que
respuesta = a != b
print(f"¿ {a} es distinto a {b} ? {respuesta}")
print()

    # Al igual que con la igualdad el operador de distinto también puede utilizarse con cadenas de texto
respuesta = "Texto1" != "Texto9" 
print(f"¿ Texto1 es distinto que Texto9 ? {respuesta}")
print()
