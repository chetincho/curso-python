# Así se define una tupla
print("Top 5 lenguajes de programación")
lenguajes = ("Python", "Java", "C#", "PHP", "Ruby")
print(lenguajes)
print()

# Tambien podemos definir una tupla de esta manera. Cunado a una variable
# le asignamos una serie de valores separados por coma Python interpreta
# automaticamente que estamos definiendo una tupla
print("Top 5 motores de base de datos")
motores_bd = "MySQL", "SQL Server", "PostgreSQL", "MongoDB", "SQLite"
print(motores_bd)
print()

# Una tupla puede contener valores de distinto tipo, incluso valores repetidos
print("Estos son los datos personales de Ana")
datos_personales = ("Ana", 20, 1.60, "Argentina", True, 20, True)
print(datos_personales)
print()

# En todos los casos los elementos que componen la tupla se muestran en pantalla 
# en el orden en la cual fueron definidos